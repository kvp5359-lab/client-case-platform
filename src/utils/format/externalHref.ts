/**
 * Нормализация ссылки ТОЛЬКО в момент открытия (клика), не при сохранении.
 *
 * Зачем: `href="example.com"` без схемы браузер трактует как ОТНОСИТЕЛЬНЫЙ путь
 * (уводит внутрь сайта), а не на внешний ресурс. Отключить это правило HTML
 * нельзя. Поэтому сохранённое значение оставляем как ввёл пользователь, а перед
 * навигацией дописываем `https://` для «голых» адресов.
 *
 * НЕ трогаем: адреса с любой схемой (https:, http:, mailto:, tel:, ftp: …),
 * якоря (#...) и относительные/абсолютные пути (/...).
 */
export function resolveExternalHref(href: string | null | undefined): string {
  const h = (href ?? '').trim()
  if (!h) return ''
  if (/^(#|\/)/.test(h)) return h
  if (/^[a-z][a-z0-9+.-]*:/i.test(h)) return h
  return `https://${h}`
}
