/**
 * «Есть ли что отправить» из композера — ОДНА формула на кнопку и на обработчик.
 *
 * Зачем общая: кнопка отправки (`MessageInput.hasContent`) и гард в
 * `useComposerSubmit.handleSend` считали это независимо, и формулы разъехались —
 * кнопка учитывала пересланные вложения, а гард нет. Итог: пересылаешь ОДИН
 * файл без текста → кнопка активна, клик молча ничего не делает, приходится
 * дописывать любой текст (жалоба владельца 2026-07-22).
 *
 * Учитывает ровно то, что реально уедет в `sendMessage`: текст, свежие файлы
 * композера и пересланные вложения. НЕ учитывает вложения редактируемого
 * черновика (`existingAttachments`) — они относятся к ветке правки, которая в
 * `handleSend` отрабатывает раньше; кнопке они нужны отдельно.
 */
export function hasComposerPayload(args: {
  hasText: boolean
  fileCount: number
  forwardedCount: number
}): boolean {
  return args.hasText || args.fileCount > 0 || args.forwardedCount > 0
}
