-- Комментарий к шаблону папки.
--
-- Зачем: одноимённые шаблоны («Банковские выписки») живут в разных наборах
-- документов с разным составом слотов, и в списке/пикере их не различить.
-- Это внутренняя пометка для сотрудников — клиенту она НЕ показывается
-- (для клиента есть description / статья базы знаний).

ALTER TABLE public.folder_templates
  ADD COLUMN IF NOT EXISTS comment text;

COMMENT ON COLUMN public.folder_templates.comment IS
  'Внутренняя пометка: чем этот шаблон отличается от одноимённых. Клиенту не показывается.';
